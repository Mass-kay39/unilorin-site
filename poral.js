let topnav = document.getElementById('stick-nav');
sticky = topnav.offsetTop;
function myFunction(){
    if(window.pageYOffset > sticky){
        topnav.classList.add('sticky')
    }
    else{
        topnav.classList.remove('sticky')
    }
}

